public class TagExpressionException extends Exception {

    public TagExpressionException() {
    }

    public TagExpressionException(String s) {
        super(s);
    }

    public TagExpressionException(String s, Throwable throwable) {
        super(s, throwable);
    }

    public TagExpressionException(Throwable throwable) {
        super(throwable);
    }
}
